package org.barcelonasoftwarecraftmanship.marsoverkata;

public class Rover {
    private Integer x;
    private Integer y;

    public Rover(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
